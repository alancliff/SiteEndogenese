                  <p>Endogênese - A palavra é de origem grega, onde ENDO significa "posição interior, movimento para dentro", e GÊNESE significa "origem, nascimento, criação, geração".</p><p>Conforme o dicionário on-line <a href="http://www.dicio.com.br/endogenese/" target="_blank" title="Dicio">Dicio</a>, Endogênese significa: "Característica de endógeno; qualidade do que se forma no interior de; processo de crescimento de uma célula no interior de outra; endogenia.". Endógeno, conforme o dicionário on-line <a href="http://michaelis.uol.com.br/moderno/portugues/index.php?lingua=portugues-portugues&palavra=end%F3geno" target="_blank" title="Michaelis">Michaelis</a>, por sua vez significa: "Que cresce de dentro de; Que se desenvolve dentro da parede de célula; Originado dentro do organismo.". Dessa forma surgiu a inspiração para o nome da empresa: Endogênese é a capacidade que certos organismos tem de se desenvolver a partir do seu interior; é o crescimento que vem de dentro. </p>
